package lib;

/**
 * Interface for all shape
 */
public interface Shape {
    /**
     * Calculates area of the Shape object
     *
     * @return Area of the Shape object
     */
    double getArea();

    /**
     * Calculates perimeter of the Shape object
     *
     * @return Perimeter of the Shape object
     */
    double getPerimeter();
}
